package com.alibaba.model;

import java.io.Serializable;
import java.util.List;

public class RankSongsData implements Serializable {

	private static final long serialVersionUID = -5180260817738619953L;

	private RankSongsDetail detail;

	private int next;

	private int page_number;

	private int previous;

	private List<RankSong> songs;

	public RankSongsDetail getDetail() {
		return detail;
	}

	public void setDetail(RankSongsDetail detail) {
		this.detail = detail;
	}

	public int getNext() {
		return next;
	}

	public void setNext(int next) {
		this.next = next;
	}

	public int getPage_number() {
		return page_number;
	}

	public void setPage_number(int page_number) {
		this.page_number = page_number;
	}

	public int getPrevious() {
		return previous;
	}

	public void setPrevious(int previous) {
		this.previous = previous;
	}

	public List<RankSong> getSongs() {
		return songs;
	}

	public void setSongs(List<RankSong> songs) {
		this.songs = songs;
	}

}
