package com.irunsin.controller.sys;


/**
 * Process结果处理类
 * 
 * @author liukaifu
 * 
 */
public interface ProcessListener
{
	/**
	 * 处理一个Process后得到的结果
	 * 
	 * @param responseBean
	 *            返回的结果对象
	 * @return 该处理器是否处理了该结果对象，如果为true表示该处理器对该结果进行了处理，
	 *         如果为false则表示该处理器没有处理该结果对象，但是该值并不影响下一个结果处理器的处理
	 * 
	 */
	public boolean onDone(ResponseBean responseBean);

}
