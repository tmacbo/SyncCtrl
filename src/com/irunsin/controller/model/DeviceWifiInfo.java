package com.irunsin.controller.model;

import java.io.Serializable;

public class DeviceWifiInfo implements Serializable{
	
	private String uuid;
	private int level;
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
		
}
